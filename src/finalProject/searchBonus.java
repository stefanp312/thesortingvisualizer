    public void fillArray(int amount) {
        //fills an array with randomly generated data
        bigArray = new int[amount];
        for (int x = 0; x < bigArray.length; x++) {
            bigArray[x] = (int) ((Math.random() * 90) + 1);
            arrayText.append(""+bigArray[x]+", ");
        
        }
    }

