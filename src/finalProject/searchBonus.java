    public void searchArray(int t) {
    //shows a window if you guessed the right now
        // does a linear search through the entire array for a certain number
        for (int x = 0; x < bigArray.length; x++) {
            if (bigArray[x] == search) {
                placeInArrayText.setText(placeInArrayText.getText() + "" + x + ", ");
            if (t==x){
                JOptionPane.showMessageDialog(null, "Win!");
                break;
            }
            }
        }
    }
